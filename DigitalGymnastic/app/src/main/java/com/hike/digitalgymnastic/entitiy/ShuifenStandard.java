package com.hike.digitalgymnastic.entitiy;

import java.io.Serializable;

//ShuifenStandard(身体水分标准):
public class ShuifenStandard implements Serializable{
	private double normalFloor;//正常身体水分下限(%)	
	private double normalCeil;//正常身体水分上限(%)
	public double getNormalFloor() {
		return normalFloor;
	}
	public void setNormalFloor(double normalFloor) {
		this.normalFloor = normalFloor;
	}
	public double getNormalCeil() {
		return normalCeil;
	}
	public void setNormalCeil(double normalCeil) {
		this.normalCeil = normalCeil;
	}
	
}
