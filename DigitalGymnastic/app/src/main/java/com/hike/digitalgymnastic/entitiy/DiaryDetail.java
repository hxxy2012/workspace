package com.hike.digitalgymnastic.entitiy;

import java.io.Serializable;
import java.util.List;

/**
 * @author changqi
 * @category 日記詳情
 */
public class DiaryDetail implements Serializable {
    long customerId;//	客户标识
    String name;//		客户名称
    String avatar;//		客户头像
    String date;//		日记日期
    short type;//		日记类型(1:运动;2:饮食;3:感受)
    String idea;//		日记想法
    String photo;//		日记照片
    short dietType;//		饮食类型(1:早餐;2:午餐;3:晚餐;4:加餐)
    String dietContent;//		饮食内容
    String sportItem;//		运动项目
    int sportDuration;//		运动时长(秒)
    String createdTime;//		创建时间
    List<PraiseCustomer> customerList;    //		称赞客户列表
    List<DiaryComment> commentList;//	日记评论列表

    public long getCustomerId() {
        return customerId;
    }

    public void setCustomerId(long customerId) {
        this.customerId = customerId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public short getType() {
        return type;
    }

    public void setType(short type) {
        this.type = type;
    }

    public String getIdea() {
        return idea;
    }

    public void setIdea(String idea) {
        this.idea = idea;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public short getDietType() {
        return dietType;
    }

    public void setDietType(short dietType) {
        this.dietType = dietType;
    }

    public String getDietContent() {
        return dietContent;
    }

    public void setDietContent(String dietContent) {
        this.dietContent = dietContent;
    }

    public String getSportItem() {
        return sportItem;
    }

    public void setSportItem(String sportItem) {
        this.sportItem = sportItem;
    }

    public int getSportDuration() {
        return sportDuration;
    }

    public void setSportDuration(int sportDuration) {
        this.sportDuration = sportDuration;
    }

    public String getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(String createdTime) {
        this.createdTime = createdTime;
    }

    public List<PraiseCustomer> getCustomerList() {
        return customerList;
    }

    public void setCustomerList(List<PraiseCustomer> customerList) {
        this.customerList = customerList;
    }

    public List<DiaryComment> getCommentList() {
        return commentList;
    }

    public void setCommentList(List<DiaryComment> commentList) {
        this.commentList = commentList;
    }

}
