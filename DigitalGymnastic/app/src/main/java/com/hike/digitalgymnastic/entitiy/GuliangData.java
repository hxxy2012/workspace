package com.hike.digitalgymnastic.entitiy;

import java.io.Serializable;

//GuliangData(骨量数据):
public class GuliangData implements Serializable{
	private GuliangStandard standard;//骨量标准	
	private short grade;//骨量评级(-1:偏低;0:标准;1:偏高)	
	private double value;//骨量值(千克)
	public GuliangStandard getStandard() {
		return standard;
	}
	public void setStandard(GuliangStandard standard) {
		this.standard = standard;
	}
	public short getGrade() {
		return grade;
	}
	public void setGrade(short grade) {
		this.grade = grade;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	
}
